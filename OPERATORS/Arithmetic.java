public class Arithmetic {
    public static void main(String[] args) {
        
        // Arithmetic opeartor

        int a = 4;
        int b = 2;

        // addition operator (+)
        System.out.println(a+b);

        // substraction operator (-)
        System.out.println(a-b);

        // multiplication operator (*)
        System.out.println(a*b);

        // division operator (/)
        System.out.println(a/b);

        // modular opeartor
        System.out.println(a%b);
    }
}
