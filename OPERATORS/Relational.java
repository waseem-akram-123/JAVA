public class Relational {
    public static void main(String[] args) {
        
        // Relational opearor

        // greater (>)
        System.out.println(3 > 2);

        // less than (<)
        System.out.println(2 < 3);

        // graeter tha equal to
        System.out.println(2 >= 2);

        // less than equal to
        System.out.println(3 <= 5);

        // equal to
        System.out.println(2 == 2);

        // not equal to
        System.out.println(2 != 2);
    }
}
